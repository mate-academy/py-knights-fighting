def battle(base_knights_config: dict) -> dict:
    # BATTLE PREPARATIONS:
    new_knights_dict = {}
    for key in base_knights_config:
        person = Knight(base_knights_config[key])
        new_knights_dict[key] = person
    # -------------------------------------------------------------------------------
    # BATTLE:

    # 1 Lancelot vs Mordred:
    Battle.start_battle(
        new_knights_dict["lancelot"], new_knights_dict["mordred"])
    # 2 Arthur vs Red Knight:
    Battle.start_battle(
        new_knights_dict["arthur"], new_knights_dict["red_knight"])

    # Return battle results:
    return Battle.get_results()
