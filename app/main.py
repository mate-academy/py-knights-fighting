KNIGHTS: KnightUser = KNIGHTS_RAW


def battle(knights_config: KnightUser) -> dict[str, int]:
    # lancelot
    lancelot = Knight(knights_config["lancelot"])
    lancelot.set_protection()
    lancelot.set_power()
    lancelot.set_potion()

    # arthur
    arthur = Knight(knights_config["arthur"])
    arthur.set_protection()
    arthur.set_power()
    arthur.set_potion()

    # mordred
    mordred = Knight(knights_config["mordred"])
    mordred.set_protection()
    mordred.set_power()
    mordred.set_potion()

    # red_knight
    red_knight = Knight(knights_config["red_knight"])
    red_knight.set_protection()
    red_knight.set_power()
    red_knight.set_potion()

    # -------------------------------------------------------------------------------
    # BATTLE:

    # 1 Lancelot vs Mordred:
    lancelot.hp -= mordred.power - lancelot.protection
    mordred.hp -= lancelot.power - mordred.protection

    # check if someone fell in battle
    if lancelot.hp <= 0:
        lancelot.hp = 0

    if mordred.hp <= 0:
        mordred.hp = 0

    # 2 Arthur vs Red Knight:
    arthur.hp -= red_knight.power - arthur.protection
    red_knight.hp -= arthur.power - red_knight.protection

    # check if someone fell in battle
    if arthur.hp <= 0:
        arthur.hp = 0

    if red_knight.hp <= 0:
        red_knight.hp = 0

    # Return battle results:
    return {
        lancelot.name: lancelot.hp,
        arthur.name: arthur.hp,
        mordred.name: mordred.hp,
        red_knight.name: red_knight.hp,
    }


print(battle(KNIGHTS))
