def battle(knights_config: dict) -> dict:
    knights: dict[str, Knight] = {}

    for name, stats in knights_config.items():
        knights[stats["name"]] = Knight(stats)

    battle_schedule = {
        "Lancelot": "Mordred",
        "Artur": "Red Knight"
    }

    for first_knight, second_knight in battle_schedule.items():
        knights[first_knight].fight(knights[second_knight])

    results_name_hp = {}

    for name, knight_instance in knights.items():
        results_name_hp[name] = knight_instance.hp

    return results_name_hp


