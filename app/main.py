def battle(knights_config: dict) -> dict:
    list_effect = ["power", "protection", "hp"]
    lancelot = knights_config["lancelot"]
    mordred = knights_config["mordred"]
    red_knight = knights_config["red_knight"]
    arthur = knights_config["arthur"]
    list_knights = [lancelot, mordred, red_knight, arthur]
    for knight in list_knights:
        knight["protection"] = 0
        for armour_knight in knight["armour"]:
            knight["protection"] += armour_knight["protection"]
    for knight_power in list_knights:
        knight_power["power"] += knight_power["weapon"]["power"]
    for knight_magic in list_knights:
        if knight_magic["potion"] is not None:
            for value in list_effect:
                if value in knight_magic["potion"]["effect"]:
                    knight_magic[value] += \
                        knight_magic["potion"]["effect"][value]
    lancelot["hp"] -= mordred["power"] - lancelot["protection"]
    mordred["hp"] -= lancelot["power"] - mordred["protection"]
    arthur["hp"] -= red_knight["power"] - arthur["protection"]
    red_knight["hp"] -= arthur["power"] - red_knight["protection"]
    for knight_result in list_knights:
        if knight_result["hp"] <= 0:
            knight_result["hp"] = 0
    return {
        lancelot["name"]: lancelot["hp"],
        arthur["name"]: arthur["hp"],
        mordred["name"]: mordred["hp"],
        red_knight["name"]: red_knight["hp"]
    }
