def battle(knights_config: dict) -> dict:
    # create knights objects
    knights = {name: Knight(data) for name, data in knights_config.items()}

    # apply armour, weapon, potion
    for knight in knights.values():
        knight.apply_equipment_and_potion()

    # battle pairing
    fights = [("lancelot", "mordred"), ("arthur", "red_knight")]

    # battle
    for k1, k2 in fights:
        knight1 = knights[k1]
        knight2 = knights[k2]

        knight1.hp -= max(0, knight2.power - knight1.protection)
        knight2.hp -= max(0, knight1.power - knight2.protection)

        knight1.hp = max(0, knight1.hp)
        knight2.hp = max(0, knight2.hp)

    # return battle results
    return {knight.name: knight.hp for knight in knights.values()}


