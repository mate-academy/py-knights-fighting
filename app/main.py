def battle(participants: dict) -> dict:

    # Selection of participants for the knight's tournament
    participating_knights = {}
    for each_knight in participants:
        nickname = each_knight  # nickname is key
        knight_details = participants[nickname]
        knight = Knight(knight_details["name"],
                        knight_details["power"], knight_details["hp"]
                        )
        participating_knights[nickname] = knight

    # Appointment of the Opponent
    appoint_opponent(participating_knights)

    # Apply skills
    apply_skills(participants, participating_knights)

    # Tournament
    return make_tournament(participating_knights)


