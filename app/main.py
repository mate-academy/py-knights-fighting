def prepare_knights(knights):
    for knight in knights.values():
        apply_equipment(knight)


def results(knights: dict) -> dict:
    return {knight.name: knight.hp for knight in knights.values()}


def battle(knightsConfig):
    # BATTLE PREPARATIONS:
    knights = {knight: create_knight(knight_stats)
               for knight, knight_stats in knightsConfig.items()}
    prepare_knights(knights)

    # -------------------------------------------------------------------------------
    # BATTLE:

    # 1 Lancelot vs Mordred:
    fight(knights["lancelot"], knights["mordred"])

    # 2 Arthur vs Red Knight:
    fight(knights["arthur"], knights["red_knight"])

    # Return battle results:
    return results(knights)


