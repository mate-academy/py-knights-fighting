def battle(knights_config: dict) -> dict:
    knight_names = ["lancelot", "arthur", "mordred", "red_knight"]

    knights = [
        Knight.from_dict(knights_config.get(name))
        for name in knight_names
    ]

    for knight in knights:
        knight.prepare_for_battle()

    # Perform battles
    knights[0].battle(knights[2])  # lancelot vs mordred
    knights[1].battle(knights[3])  # arthur vs red_knight

    return build_result(knights)


def build_result(knights: list[Knight]) -> dict:
    return {knight.name: knight.hp for knight in knights}
