def battle(knights_config: dict) -> dict:
    knights = {}

    for knight, values in knights_config.items():
        name = values["name"]

        hp = values["hp"]

        power = values["power"]

        protection = sum([equip["protection"] for equip in values["armour"]])

        power += values["weapon"]["power"]

        if values["potion"] is not None:
            for effect, value in values["potion"]["effect"].items():
                if effect == "power":
                    power += value
                if effect == "protection":
                    protection += value
                if effect == "hp":
                    hp += value

        knights[knight] = Knight(name, power, hp, protection)

    Knight.battle(knights["lancelot"], knights["mordred"])
    Knight.battle(knights["arthur"], knights["red_knight"])

    return {str(value.name): value.hp for value in knights.values()}


