def battle(knights: dict) -> dict:
    list_knights = []
    for key, value in knights.items():
        knight = Character(value["name"], value["power"], value["hp"])
        knight.use_weapon(value["weapon"]["power"])
        knight.use_armour(value["armour"])
        knight.use_potion(value["potion"])
        list_knights.append(knight)

    fight_1 = Fight.fight(list_knights[0], list_knights[2])
    fight_2 = Fight.fight(list_knights[1], list_knights[3])

