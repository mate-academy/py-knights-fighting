def battle(knights_config: dict[dict]) -> dict:
    # BATTLE PREPARATIONS:

    # Knight class initialisation, store in knights dict
    knights = {}
    for key, value in knights_config.items():
        knights[key] = Knight(value["name"], value["power"], value["hp"],
                              value["armour"], value["weapon"],
                              value["potion"])

    # apply armour, weapon, potion if exist
    for value in knights.values():
        value.apply_armour()
        value.apply_weapon()
        value.apply_potion_if_exist()

    # -------------------------------------------------------------------------------
    # BATTLE:

    # 1 Lancelot vs Mordred:
    KnightBattle.battle(knights["lancelot"], knights["mordred"])

    # 2 Arthur vs Red Knight:
    KnightBattle.battle(knights["arthur"], knights["red_knight"])

    # # Return battle results:
    return {
        knights["lancelot"].name: knights["lancelot"].hp,
        knights["arthur"].name: knights["arthur"].hp,
        knights["mordred"].name: knights["mordred"].hp,
        knights["red_knight"].name: knights["red_knight"].hp,
    }


print(battle(KNIGHTS))
