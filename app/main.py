def battle(knights_config: dict) -> dict:
    knights = {}
    for knight, stats in knights_config.items():
        knights[knight] = KnightsStats(stats).apply_equipments()

    lancelot = knights["lancelot"].stats
    arthur = knights["arthur"].stats
    mordred = knights["mordred"].stats
    red_knight = knights["red_knight"].stats

    Battle(lancelot, mordred).battle()
    Battle(arthur, red_knight).battle()

    return {
        lancelot["name"]: lancelot["hp"],
        arthur["name"]: arthur["hp"],
        mordred["name"]: mordred["hp"],
        red_knight["name"]: red_knight["hp"]
    }


